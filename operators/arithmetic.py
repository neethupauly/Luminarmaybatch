# (+, -, *, /, //,%,**)

no1 = int(input("enter the num1"))
no2 = int(input("enter the num2"))

# addition
res = no1+no2
print("add res", res)

# subtraction
res = no1-no2
print("sub res", res)

# multiplication
res = no1*no2
print("multiplied res", res)

# division
res = no1/no2
print("divided res", res)

# floor division
res = no1//no2
print("floor division res", res)

# modulous remainder
res = no1%no2
print("modulo res", res)

# exponential
res = no1**no2
print("exponential res", res)
